function btnpost() {
    var storage = window.localStorage;
    //获取学校的ID

    var selectindex = $('.school_ready p').attr('id');
    var school = $('.school input').val();
    var user = $(".user input").val();
    storage.setItem("enterprise_count", user);
    var password = $(".password input").val();
    storage.setItem("enterprise_password", password);
    $(".input").removeClass("border-r");
    if (school == '') {
        $(".school input").focus();
        $(".school").addClass("border-r");
        $(".prompt").text("您还没有填写学校名称！")
    }
    if (user == "") {
        $(".user input").focus();
        $(".user").addClass("border-r");
        $(".prompt").text("您还没有填写企业账号！")
    }
    if (password == "") {
        $(".password input").focus();
        $(".password").addClass("border-r");
        $(".prompt").text("您还没有填写企业密码！")
    } else {
        $(".prompt").text("登录中......");

        $.ax({
            url: apiUrl+'mobile/login',
            type: 'GET',
            datatype: 'json',
            data: {
                schoolId: 1,
                username: user,
                password: password,
                role: 1
            },
            success: function (data) {
                if (data.code === 0) {
                    setCookie("token",data.data);
                    //window.location.href = 'http://101.37.27.185:8080/notice.html';
                    window.location.href = 'http://101.37.27.185:8080/dingang/admin/C_organization.html';
                }
                else {
                    my_alert('提示(' + data['msg'] + ')', data['msg']);
                    return false;
                }
            }
        })
    }
}

